function isEven (number){
    if (number % 2===0){
        return true;
    }
    else{
        return false;
    }
}
console.log(isEven(4))
console.log(isEven(9))
function isOdd(number){
    if (number % 2 === 1){
        return true
    }
    return false
}
console.log(isOdd(7))
console.log(isOdd(3))